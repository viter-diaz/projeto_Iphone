package projeto_Iphone;

public class musica {
	   private String nome;
       private String caminho;

	    public musica(String nome, String caminho) {
	        this.nome = nome;
	        this.caminho = caminho;
	    }

	    public String getNome() {
	        return nome;
	    }

	    public String getCaminho() {
	        return caminho;
	    }
}
